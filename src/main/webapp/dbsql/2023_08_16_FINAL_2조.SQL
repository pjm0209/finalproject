CREATE OR REPLACE TRIGGER TRIGGER_INTOSTOCK_INSERT
AFTER INSERT ON INTOSTOCK
FOR EACH ROW
BEGIN
  UPDATE STOCK
  SET STOCK_QTY = STOCK_QTY + :NEW.INTOSTOCK_QTY
  WHERE BOOK_NO = :NEW.BOOK_NO;
END;

